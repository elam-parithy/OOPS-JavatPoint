package Constructor;

public class Student2 {

	  int id;  
	  String name;  
	    
	    //creating a parameterized constructor  
	    Student2(int i,String n){  
	    id = i;  
	    name = n;  
	    }  
	    
	  //method to display the values  
	    void display(){System.out.println(id+" "+name);}  
}
